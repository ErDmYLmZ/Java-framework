package personalStudy;

public class PrintEvenNum {

	public static void main(String[] args) {
		System.out.print("Even Numbers from 20 to 0 are: "+20+" ");
		
		int i=20;
        while (i >= 2) {
        	
            System.out.print(i - 2+" ");
            i-=2;
		
		
		
		
	}

}
}